# 정수 나누기 연산자
print("5/7 = ",5/7)
print("5//7 = ", 5//7)
print("5//2 = ", 5//2, "\n") # 정수 부분만 남깁니다.

# 나머지 연산자
print("5 % 2 = ",5%2,"\n") # 나머지를 보여줍니다.

# 제곱 연산자
print("3**2 = ",3**2,"\n")

# 연산자의 우선순위
print("2-2+2/2*2+3 = ",2-2+2/2*2+3,"\n") 
# 평소 사칙연산과 우선순위가 같다. 그러나 될 수 있으면 괄호를 쓰도록 하자.
print("안녕"+"하세요"*3)
# 문자열 연산자 또한 우선순위가 있다.


